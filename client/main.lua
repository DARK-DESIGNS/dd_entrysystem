RegisterNetEvent('dd_entrysystem:notify', function(data)
    local text = data
    TriggerEvent('qb-core:client:DrawText', text, 'left')
    Wait(5000) -- display text for 5 seconds
    TriggerEvent('qb-core:client:HideText')
end)

function Draw3DText(x, y, z, scl_factor, text)
    local onScreen, _x, _y = World3dToScreen2d(x, y, z)
    local p = GetGameplayCamCoords()
    local distance = GetDistanceBetweenCoords(p.x, p.y, p.z, x, y, z, 1)
    local scale = (1 / distance) * 2
    local fov = (1 / GetGameplayCamFov()) * 100
    local scale = scale * fov * scl_factor
    if onScreen then
        SetTextScale(0.0, scale)
        SetTextFont(0)
        SetTextProportional(1)
        SetTextColour(255, 255, 255, 255)
        SetTextDropshadow(0, 0, 0, 0, 255)
        SetTextEdge(2, 0, 0, 0, 150)
        SetTextDropShadow()
        SetTextOutline()
        SetTextEntry("STRING")
        SetTextCentre(1)
        AddTextComponentString(text)
        DrawText(_x, _y)
    end
end

RegisterNetEvent('dd_entrysystem:joinentry', function(source, data)

    if data == check then
        if isStaff == false then
            isStaff = true
            oldPlayerPos = GetEntityCoords(playerPed)
            SetEntityCoords(playerPed, Config.staffSpawn, true)
            TriggerEvent("dd_entrysystem:notify", Config.staffJoinMessage)
        else
            isStaff = false
            TriggerEvent("dd_entrysystem:notify", Config.staffLeaveMessage)
            SetEntityCoords(playerPed, oldPlayerPos, true)
        end
    else
        TriggerEvent("dd_entrysystem:notify", Config.noPermission)
    end
end)

